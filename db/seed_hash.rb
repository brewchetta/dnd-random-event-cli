require "lolize"
rainbow = Lolize::Colorizer.new

events.each do |event|
  e = Event.new
  e.title = event[:title]
  e.description = event[:description]
  e.rarity = event[:rarity]
  e.save
  event[:tags].each do |tag|
    e.tags << Tag.find_or_create_by(name: tag)
  end
  rainbow.write "Created #{e.title}..."
end
